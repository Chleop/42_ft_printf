#include "ft_printf.h"

void	ft_string(char *s, int *j)
{
	if (s)
	{
		ft_putstr(s);
		(*j) = (*j) + ft_strlen(s);
	}
	else
	{
		write(1, "(null)", 6);
		(*j) = (*j) + 6;
	}
}
